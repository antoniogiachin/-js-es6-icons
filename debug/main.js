// Funzione estrazione tipi da array di oggetti-icone
function extractTypes(array){

    const types = [];

    array.forEach(element => {
        types.push(element.type);
    });

    console.log(types);

    const values = ['all'];

    types.forEach(element =>{

        if(!values.includes(element)){

            values.push(element);

        }

    });

    return values;
}

